"use strict";

/* 1) 변수의 선언과 할당 */
// 변수의 선언
var myNumber1;

// 할당
myNumber1 = 100;
console.log(myNumber1);


/* 2) 변수의 선언과 할당 통합 */
var myNumber2 = 123;
console.log(myNumber2);


/* 3) 변수값 변경하기 */
// 한 번 만들어진 변수는 다른 값으로 교체 가능
var myNumber2 = 456;
console.log(myNumber2);


/* 3) 변수의 재선언 */
var myNumber3 = 300;
console.log(myNumber3);

var myNumber3 = 789;
console.log(myNumber3);

